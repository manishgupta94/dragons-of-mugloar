package game;

public class NameAbilityHelper {

	private String ability;

	private int value;

	public NameAbilityHelper(String ability, int value) {
		this.ability = ability;
		this.value = value;
	}

	public String getAbility() {
		return this.ability;
	}

	public int getValue() {
		return this.value;
	}
}
